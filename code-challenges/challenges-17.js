// Required:
// 
// A phrase is considered palindrome if, after converting all uppercase letters into lowercase letters
// and removing all non-alphanumeric characters, it reads the same forward and backward.
// Alphanumeric characters include letters and numbers.
// Given a string s, return true if it is a palindrome, or false otherwise.
// 
// Input: s = "A man, a plan, a canal: Panama"
// Output: true
// as you can see "amanaplanacanalpanama" is a palindrome.
//

const isPalindrome = (str) => {

    const prep_regex = /[^a-zA-Z0-9]/g,

        lowerStr = str.toLowerCase().replace(prep_regex, ""),

        check_palindrome = lowerStr.split("").reverse().join("");

    if (lowerStr === check_palindrome) {
        return true
    } else {
        return false
    }
}
